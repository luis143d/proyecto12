    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int numero = Integer.parseInt(numeroTextField.getText());
        int suma = 0;
        int producto = 1;
// Extraer las cifras y realizar los c√°lculos
        for (int i = 0; i < 4; i++) {
            int cifra = numero % 10;
            suma += cifra;
            producto *= cifra;
            numero /= 10;
        }
        sumaLabel.setText("Suma de cifras: " + suma);
        productoLabel.setText("Producto de cifras: " + producto);
    }//GEN-LAST:event_jButton1ActionPerformed

